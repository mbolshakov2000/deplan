import java.util.*;

public class Schedule {
    String id_teacher;
    String name_teacher;
    String id_event;
    String name_event;

    Schedule(String id_teacher, String name_teacher, String id_event, String name_event) {
        this.id_teacher = id_teacher;
        this.name_teacher = name_teacher;
        this.id_event = id_event;
        this.name_event = name_event;
    }

}
